                                    Admissions to the Ph.D programs offered by the Institute in various areas / disciplines are based on academic performance and performance in the written test and /or interview conducted by the institute.
The Institute offers Ph.D programs in the core ares of engineering namely electrical, electronics, computer science and mecanical engineering and allied areas of mathematics and physics. Faculty members of the institute are doctoral degree holders from institutions of repute and scholars can register for full time Ph.D under the guidance of institute faculty members. Research scholars can work in areas of mutual interest to them and to the faculty members, whose area(s) of interest / specialization are listed in the individual faculty profile. Research scholars are generally admitted twice in a year (July / January sessions) based on performance of the candidates in written and / or interview conducted by the institute. </p>
